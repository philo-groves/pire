import { execFile } from "node:child_process";
import { access, mkdtemp, readFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { promisify } from "node:util";
import { describe, expect, test } from "vitest";
import { scorePireEvalSessionFromFiles } from "../src/core/pire/eval-runner.js";

const execFileAsync = promisify(execFile);
const PACKAGE_ROOT = process.cwd();
const FIXTURE_DIR = join(PACKAGE_ROOT, "test", "fixtures", "pire-evals");

describe("pire eval scaffold cli", () => {
	test("writes a scaffolded proof-gap case that scores through the eval runner", async () => {
		const casesDir = await mkdtemp(join(tmpdir(), "pire-eval-scaffold-"));
		const caseName = "scaffold-broker-proof-gap";
		const suitePath = join(FIXTURE_DIR, "deep-scenario-suite.json");

		const result = await execFileAsync(
			"npx",
			[
				"tsx",
				"./src/pire-eval-scaffold-cli.ts",
				"--suite",
				suitePath,
				"--cases-dir",
				casesDir,
				"--task-id",
				"binre-scenario-006",
				"--preset",
				"proof-gap",
				"--case-name",
				caseName,
			],
			{
				cwd: PACKAGE_ROOT,
			},
		);

		const caseDir = join(casesDir, caseName);
		expect(result.stdout.trim()).toBe(caseDir);

		await access(join(caseDir, "case.json"));
		await access(join(caseDir, "bindings.json"));
		await access(join(caseDir, ".pire", "artifacts.json"));
		await access(join(caseDir, ".pire", "session", "findings.json"));

		const [caseJson, bindingJson, findingsJson] = await Promise.all([
			readFile(join(caseDir, "case.json"), "utf-8"),
			readFile(join(caseDir, "bindings.json"), "utf-8"),
			readFile(join(caseDir, ".pire", "session", "findings.json"), "utf-8"),
		]);

		expect(caseJson).toContain("Proof Gap Scaffold");
		expect(bindingJson).toContain('"taskId": "binre-scenario-006"');
		expect(bindingJson).toContain("starter scaffold generated by pire-eval-scaffold");
		expect(findingsJson).toContain("Starter scaffold for binre-scenario-006 using the proof-gap preset");

		const scored = await scorePireEvalSessionFromFiles({
			cwd: caseDir,
			suitePath,
			bindingsPath: join(caseDir, "bindings.json"),
		});

		expect(scored.score.taskScores).toHaveLength(1);
		expect(scored.score.taskScores[0]?.issues).toEqual([
			"ctf task requires captured flag evidence for FLAG_BROKER_PRIV_ACTION",
		]);
	});
});
